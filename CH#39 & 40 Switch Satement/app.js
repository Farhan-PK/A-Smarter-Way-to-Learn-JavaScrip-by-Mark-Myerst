var day = +prompt("Enter Your day Number:", "1....7");
switch (day) {
  case 1:
    alert("Hello Sunday!");
    break;
  case 2:
    alert("Hello Monday");
    break;
  case 3:
    alert("Hello TuesDay!");
    break;
  case 4:
    alert("Hello Wednesday");
    break;
  case 5:
    alert("Hello Thursday");
    break;
  case 6:
    alert("Hello Friday!");
    break;
  case 7:
    alert("Hello Saturday");
    break;
  default:
    alert("Select right Number");
    break;
}
